% --- Executes on button press in button.
function button_Callback(hObject, eventdata, handles)
% hObject    handle to button (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
Qexp= str2double(get(handles.Qexp,'string'));
Lf= str2double(get(handles.Lf,'string'));
Df= str2double(get(handles.Df,'string'));
k= str2double(get(handles.k,'string'));
ctc= str2double(get(handles.ctc,'string'));
Tb= str2double(get(handles.Tb,'string'));
Tinf= str2double(get(handles.Tinf,'string'));
h= str2double(get(handles.h,'string'));
w= str2double(get(handles.w,'string'));
l= str2double(get(handles.l,'string'));

%Calculating Areas
Aplate = w*l;   %Area of the plate
n = floor(Aplate/ctc^2);    %Number of Fins
Afin = n*(pi*Df*Lf +((pi*Df^2)/4));    %Calculating the area of the fins for cylindrical pins
Aunfin= Aplate-n*(pi*Df^2/4);    %Calculating the area of the plate behind the fins

%Heat Transgfer Calculations
Qfin = h*Afin*(Tb-Tinf);    %calculating the heat transfer of the Radiator Fin
Qplate = h*Aunfin*(Tb-Tinf);    %calculating heat transfer from the rest of the plate
Qmax = Qfin+Qplate; %total max heat transfer for system

%Calculating Effeciency
Eff= Qexp/Qmax;
Eff= Eff*100;   %calculating for percent

set(handles.answer,'string',Eff) 


