% --- Executes on button press in rectbutton.
function rectbutton_Callback(hObject, eventdata, handles)
% hObject    handle to rectbutton (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
Qexp= str2double(get(handles.Qexp,'string'));
Lf= str2double(get(handles.Lf,'string'));
Df= str2double(get(handles.Df,'string'));
k= str2double(get(handles.k,'string'));
ctc= str2double(get(handles.ctc,'string'));
Tb= str2double(get(handles.Tb,'string'));
Tinf= str2double(get(handles.Tinf,'string'));
h= str2double(get(handles.h,'string'));
w= str2double(get(handles.w,'string'));
l= str2double(get(handles.l,'string'));
rectL= str2double(get(handles.rectlength,'string'));

rectT= str2double(get(handles.rectthickness,'string'));
rectW= str2double(get(handles.rectwidth,'string'));

%%For Rectangular Fins
Aplate= w*l;
n = floor(Aplate/ctc^2);    %Number of Fins
rectT= str2double(get(handles.rectthickness,'string'))
rectW= str2double(get(handles.rectwidth,'string'))


%%For Rectangular Fins
%Calculating Areas

Aplate = w*l;   %Area of the plate
AfinR = n*(4*rectW*rectL+rectW*rectT);      %Calculating the Area of the Fins for Rectangular Fins
AunfinR= Aplate-n*(rectL*rectT);            %Calculating the Area of the Plate Behind the Fins

%Heat Transgfer Calculations
QfinR = h*AfinR*(Tb-Tinf);       %Calculating the Heat Transfer of the Radiator Fin
QplateR = h*AunfinR*(Tb-Tinf);   %Calculating the Heat Transfer From the Rest of the Plate
QmaxR = QfinR+QplateR;           %Total Max Heat Transfer for System

%Calculating Effeciency of Rectangular System
EffR= Qexp/QmaxR;
EffR= EffR*100;   %Calculating for Percent

set(handles.answer,'string',EffR)







